function love.conf(t)

    t.window.title = 'Game'
    t.window.width = 0 -- Define a largura como 0 para usar a largura da tela do monitor
    t.window.height = 0 --Define a altura como 0 para usar a altura da tela do monitor
    t.window.fullscreen = false -- Ativa o modo de tela cheia
    t.window.fullscreentype = 'desktop' -- Use o modo de tela cheia do sistema operacional

end
